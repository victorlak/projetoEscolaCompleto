    private void limparCampos(){
        edtCpf.setText("");
        edtIdade.setText("");
        edtMatricula.setText("");
        edtNome.setText("");
        
        habilitarCampos(false);
    }
    private void objetoParaCampos(Aluno aluno){
        edtCpf.setText(aluno.getCpf());
        edtIdade.setText(aluno.getIdade());
        edtMatricula.setText(aluno.getMatricula());
        edtNome.setText(aluno.getNome());
    }
    private void habilitarCampos(Boolean habilitar){
        edtCpf.setEnabled(habilitar);
        edtIdade.setEnabled(habilitar);
        edtMatricula.setEnabled(habilitar);
        edtNome.setEnabled(habilitar);
    }
    private void atualizarTabela(){
        List<Aluno> alunos = this.alunoController.findAllAluno();
        alunos.toString();
        TMCadAluno tabelaAluno = new TMCadAluno(alunos);
        grdAlunos.setModel(tabelaAluno);
    }

